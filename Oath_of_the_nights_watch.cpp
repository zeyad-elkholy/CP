#include <algorithm>
#include <iostream>
#include <vector>
int main (int argc, char *argv[]) {
  int n;
  std::cin >> n;
  std::vector<long long>N(n);
  for (int i = 0; i < n; i++) {
    std::cin >> N[i];
  }
  long long ans = 0;
  std::sort(N.begin(), N.end());
  for (int i = 0; i < n; i++) {
    if (N[i] == N[0] || N[i] == N[n - 1] ) continue;
    ans++;
  }
  std::cout << ans << std::endl;

  return 0;
}

